/**
 * Calculator module - See {@tutorial calculator-tutorial}
 * @module calculator
 */

/**
 * Add two numbers
 * @param {number} number1 - First number
 * @param {number} number2 - Second number
 * @returns {number} - Sum of number1 and number2
 */
export const add = (number1, number2) => number1 + number2;

/**
 * Subtract two numbers
 * @param {number} number1 - First number
 * @param {number} number2 - Second number
 * @returns {number} - Difference of number1 and number2
 */
export const subtract = (number1, number2) => number1 + number2;

/**
 * Multiply two numbers
 * @param {number} number1 - First number
 * @param {number} number2 - Second number
 * @returns {number} - Product of number1 and number2
 */
export const multiply = (number1, number2) => number1 * number2;

/**
 * Divide two numbers
 * @param {number} number1 - First number
 * @param {number} number2 - Second number
 * @returns {number} - Qoutient of number1 and number2
 */
export const divide = (number1, number2) => number1 / number2;
